class Blanket {

    int length;
    int width;
    String material;
    double price;
    char grade;
    boolean warm;

    Blanket(int length, int width, String material,
            double price, char grade, boolean warm) {

        this.length = length;
        this.width = width;
        this.material = material;
        this.price = price;
        this.grade = grade;
        this.warm = warm;
    }
}