class Maps {
    String country;
    double length;
    double width;
    String scale;
    String language;
    String regionCovered;

    Maps(String country, double length, double width, String scale, String language, String regionCovered) {
        this.country = country;
        this.length = length;
        this.width = width;
        this.scale = scale;
        this.language = language;
        this.regionCovered = regionCovered;
    }

  
}