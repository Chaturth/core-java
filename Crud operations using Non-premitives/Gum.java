class Gum {

    String brand;
    String flavor;
    double price;
    int quantity;
    boolean sugarFree;
    String packagingType;

    Gum(String brand, String flavor, double price, int quantity, boolean sugarFree, String packagingType) {
        this.brand = brand;
        this.flavor = flavor;
        this.price = price;
        this.quantity = quantity;
        this.sugarFree = sugarFree;
        this.packagingType = packagingType;
    }
}