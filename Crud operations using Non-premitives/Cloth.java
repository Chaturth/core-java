class Cloth {

    String type;
    String brand;
    double price;
    String size;
    String color;
    String material;

    Cloth(String type, String brand, double price, String size, String color, String material) {
        this.type = type;
        this.brand = brand;
        this.price = price;
        this.size = size;
        this.color = color;
        this.material = material;
    }
}