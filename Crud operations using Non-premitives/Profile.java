class Profile {

    String name;
    int age;
    String email;
    String phone;
    String city;
    String profession;

    Profile(String name, int age, String email, String phone, String city, String profession) {
        this.name = name;
        this.age = age;
        this.email = email;
        this.phone = phone;
        this.city = city;
        this.profession = profession;
    }
}