class Farmer{
	
	String name;
	String location;
	String cropGrown;
	int area;
	double acers;
	String soilType;
	
	Farmer(String name,String location,String cropGrown,int area,double acers,String soilType)
	{
		this.name=name;
		this.location=location;
		this.cropGrown=cropGrown;
		this.area=area;
		this.acers=acers;
		this.soilType=soilType;
	}
}
