class Statue {

    String name;
    String location;
    double height;
    String material;
    String sculptor;
    int yearBuilt;

    Statue(String name, String location, double height, String material, String sculptor, int yearBuilt) {
        this.name = name;
        this.location = location;
        this.height = height;
        this.material = material;
        this.sculptor = sculptor;
        this.yearBuilt = yearBuilt;
    }
}